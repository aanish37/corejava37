package Constructor;

public class constructorDocs {

	/*
	 * -------------- Constructor---------------------
	 * # use to create object in java.
	 * # special type of method which has same as class name is known as constructor.
	 * # if there is no any other user defined constructor in class then java provides default constructor.
	 * 
	 * # no return type.
	 * 
	 * # types :
	 *   1. default constructor:
	 *   ->  use to create object and set default values to it.
	 *   # syntax:
	 *          class_name(){
	 *                //set default values to object
	 *          }
	 *          
	 *   2. Parameterized constructor:
	 *   -> use to create object and set dynamic values to it.
	 *   # syntax :
	 *     class_name(arg1,arg2,.......,argn){
	 *               //set dynamic values to object
	 *     }
	 *        
	 */
}
