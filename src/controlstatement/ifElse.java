package controlstatement;

public class ifElse {
	/*
	 * ---------------------if-else-------------------------------
	 * # syntax:
	 * if(condition){
	 *     //statements
	 *  }else{
	 *     //statements
	 *  }  
	 */
	public static void main(String[] args) {
		int n = -3434;
		
		if(n > 0){
			System.out.println(n+ "is +ve number");
		}
		else {
			System.out.println(n+ "is -ve number");
		}
	}

}

